package sessions2;

public class hinh3 {
    public static void main(String[] args) {
        for(int i=0;i<8; i++)
        {
            if(i == 0 || i==7){
                System.out.println("+------+");
            }
            if ( i==1){
                System.out.println("|\\..../|");
            }
            if ( i==2){
                System.out.println("| \\../ |");
            }
            if (i ==3){
                System.out.println("|  \\/  |");
            }
            if (i==4){
                System.out.println("|  " + "/"+""+"\\  |");
            }
            if ( i==5){
                System.out.println("| /..\\ |");
            }
            if ( i==6){
                System.out.println("|/....\\|");
            }
        }
    }
}
